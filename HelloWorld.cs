namespace principal
{
    internal class HelloWorld
    {
        /**
        * Este es un programa simple en C# que imprime "Hello, World!" en la consola.
        * El método Main es el punto de entrada del programa, donde se ejecuta el código
        * para mostrar el mensaje en la consola.
        */
        public static void Main(string[] args)
        {
            Console.Write("Hello"); // Imprime "Hello " sin salto de línea
            Console.Write(", World! \n"); // Imprime ", World!" con salto de línea, por lo que el resultado final será "Hello , World!" en la consola
            Console.WriteLine("Iniciando el programa..."); // Imprime "Iniciando el programa..." en una nueva línea


        }
    }
}